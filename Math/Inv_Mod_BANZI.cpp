int MInt<0>::Mod = 998244353;
 
constexpr int P = 1000000007;
using Z = MInt<P>;
 
struct Comb {
    int n;
    std::vector<Z> _fac;
    std::vector<Z> _invfac;
    std::vector<Z> _inv;
    
    Comb() : n{0}, _fac{1}, _invfac{1}, _inv{0} {}
    Comb(int n) : Comb() {
        init(n);
    }
    
    void init(int m) {
        m = std::min<int>(m, Z::getMod() - 1);
        if (m <= n) return;
        _fac.resize(m + 1);
        _invfac.resize(m + 1);
        _inv.resize(m + 1);
        
        for (int i = n + 1; i <= m; i++) {
            _fac[i] = _fac[i - 1] * i;
        }
        _invfac[m] = _fac[m].inv();
        for (int i = m; i > n; i--) {
            _invfac[i - 1] = _invfac[i] * i;
            _inv[i] = _invfac[i] * _fac[i - 1];
        }
        n = m;
    }

    // 获取 m 的阶乘
    Z fac(int m) {
        if (m > n) init(2 * m);
        return _fac[m];
    }

    // 获取 m 的阶乘逆元
    Z invfac(int m) {
        if (m > n) init(2 * m);
        return _invfac[m];
    }

    // 获取 m 的模逆
    Z inv(int m) {
        if (m > n) init(2 * m);
        return _inv[m];
    }

    // 计算二项式系数 (n 取 m) 即组合数 C(n, m)
    Z binom(int n, int m) {
        if (n < m || m < 0) return 0;
        return fac(n) * invfac(m) * invfac(n - m);
    }
} comb;

void solve() {
    int n, k;
    cin >> n >> k;

    for (int i = 0; i < k; i++) {
        int r, c;
        cin >> r >> c;
        if (r == c) {
            n--;
        } else {
            n -= 2;
        }
    }

    Z ans = 0, res = 1;

    for (int i = 0; 2 * i <= n; i++) {
        if (i > 0) {
            res *= (2 * i - 1) * 2;
        }
        ans += comb.binom(n, 2 * i) * res;
    }

    cout << ans << '\n';
}

